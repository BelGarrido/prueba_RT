#include "libft.h"
#include <stdint.h>

uint32_t	ft_swap_u32(uint32_t v)
{
	return ((v >> 24) | ((v >> 8) & 0x0000FF00U)
		| ((v << 8) & 0x00FF0000U) | (v << 24));
}
